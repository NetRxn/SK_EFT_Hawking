"""Paper tables framework (Phase 5v).

Single rendering engine for all paper tables. Each paper declares its
tables in `papers/<paper_key>/tables.py` as a dict of specs:

    from scripts.paper_tables import Col
    from scripts.paper_tables.sources import platform_rows

    TABLES = {
        'table1_platforms': {
            'description': 'Parameters at the sonic horizon',
            'rows': lambda: platform_rows(['Steinhauer', 'Heidelberg', 'Trento']),
            'columns': [
                Col('parameter',  'Parameter', align='l'),
                Col('unit',       'Units'),
                Col('Steinhauer', 'Steinhauer ($^{87}$Rb)'),
                Col('Heidelberg', 'Heidelberg ($^{39}$K)'),
                Col('Trento',     'Trento ($^{23}$Na)'),
            ],
        },
    }

The renderer emits a complete `\\begin{tabular}...\\end{tabular}` block
per spec to `papers/<paper_key>/tables/<spec_id>.tex`, ready to be
\\input{} from the paper's main .tex. Paper .tex keeps the `\\begin{table}`
envelope (caption, label, layout) but offloads data content to the
autogenerated file.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Callable


@dataclass
class Col:
    """Column specification for a paper table.

    field   — key to extract from each row dict.
    header  — LaTeX-formatted header cell (no escaping; authors' job).
    align   — LaTeX column alignment ('l', 'c', 'r'). Defaults to 'c'.
    """
    field: str
    header: str
    align: str = 'c'

    def cell(self, row: dict) -> str:
        """Extract the cell value from a row dict. Values should already
        be formatted strings from the source function (precision, units,
        etc. are the source's responsibility so each physics quantity
        can choose its natural formatting)."""
        v = row.get(self.field)
        if v is None:
            return '---'
        return str(v)


def render_table(spec: dict) -> str:
    """Render a single table spec to a LaTeX tabular block.

    Returns a string ending in a newline, suitable for writing to a
    `.tex` snippet the paper can `\\input{}`.
    """
    rows_fn: Callable[[], list[dict]] = spec['rows']
    columns: list[Col] = spec['columns']
    rows = rows_fn()

    align_spec = ''.join(c.align for c in columns)
    lines: list[str] = []
    lines.append('% AUTOGENERATED by scripts/render_paper_tables.py — do not edit by hand.')
    lines.append('% Regenerate via `uv run python scripts/render_paper_tables.py` after')
    lines.append('% changing any source data or the paper\'s tables.py spec.')
    desc = spec.get('description')
    if desc:
        lines.append(f'% Description: {desc}')
    lines.append(r'\begin{tabular}{' + align_spec + '}')
    lines.append(r'\hline')
    lines.append(' & '.join(c.header for c in columns) + r' \\')
    lines.append(r'\hline')
    for row in rows:
        lines.append(' & '.join(c.cell(row) for c in columns) + r' \\')
    lines.append(r'\hline')
    lines.append(r'\end{tabular}')
    return '\n'.join(lines) + '\n'
